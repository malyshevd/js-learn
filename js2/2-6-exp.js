'use strict'
/**
* Функция суммы переданных параметров 
* @param {number} a
* @param {number} b
* @returns {number}
*/
function addition(a, b) {
    return a + b;
}

/**
* Функция вычитания переданных параметров 
* @param {number} a
* @param {number} b
* @returns {number}
*/
function subtraction(a, b) {
    return a - b;
}

/**
* Функция произведения переданных параметров 
* @param {number} a
* @param {number} b
* @returns {number}
*/
function multiplication(a, b) {
    return a * b;
}

/**
* Функция деления переданных параметров 
* @param {number} a
* @param {number} b
* @returns {number}
*/
function division(a, b) {
    return a / b;
}